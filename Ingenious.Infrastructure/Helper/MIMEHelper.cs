        /// 根据ContentType获取文件扩展名(例如: "image/png" => ".png")
        /// </summary>
        /// <param name="contentType"></param>
        /// <returns></returns>
        public static string GetFileType(string contentType)
        {
            string filename = string.Empty;
            var list = mime.Where(item => item.Value == contentType.ToLower());
            return list.Count() == 0 ? "" : list.First().Key;
        }
